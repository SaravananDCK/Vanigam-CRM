    public static string GetUnicode(string iconName)
    {
        string unicode = GetString(iconName);
        if (string.IsNullOrEmpty(iconName))
        {
            return iconName;
        }
        // Convert the hex string to an integer, then to a char
        int code = 0;
        int.TryParse(unicode, System.Globalization.NumberStyles.HexNumber, null, out code);
        if (code == 0)
        {
            return iconName;
        }
        return char.ConvertFromUtf32(code); // Return the actual character

    }
    public static string GetString(string iconName)
    {
        if (iconName.StartsWith("far ")) iconName = iconName.Replace("far ", "");
        if (iconName.StartsWith("fas ")) iconName = iconName.Replace("fas ", "");
        if (iconName.StartsWith("fad ")) iconName = iconName.Replace("fad ", "");
        if (iconName.StartsWith("fab ")) iconName = iconName.Replace("fab ", "");
        if (iconName.StartsWith("fal ")) iconName = iconName.Replace("fal ", "");
        if (Unicode.TryGetValue(iconName.Trim(), out var unicode))
        {
            return unicode;
        }
        return string.Empty;
    }
